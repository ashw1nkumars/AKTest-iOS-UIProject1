import Foundation

class UserObject {
    let firstName: String
    let lastName: String
    let age: Int
    let image: String
    
    init(firstName: String, lastName: String, age: Int, image: String) {
        self.firstName = firstName
        self.lastName = lastName
        self.age = age
        self.image = image
    }
}
